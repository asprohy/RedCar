public class HallInfo {

    int x;
    int y;
    int hallCount;
    int roadId;
    String channel;

    public HallInfo(int x, int y, int hallCount, int roadId, String channel){
        this.x = x;
        this.y = y;
        this.hallCount = hallCount;
        this.roadId = roadId;
        this.channel= channel;
    }
}
