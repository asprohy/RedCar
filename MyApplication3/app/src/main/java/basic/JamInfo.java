public class JamInfo {
    public int x;
    public int y;
    public int nextX;
    public int nextY;

    public JamInfo(int x, int y, int nextX, int nextY){
        this.x = x;
        this.y = y;
        this.nextX = nextX;
        this.nextY = nextY;
    }
}
